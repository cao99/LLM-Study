from mcp.server.fastmcp import FastMCP

# ==============================================================================
# 1. 使用 FastMCP 实例化“大脑”
# FastMCP 是官方推荐的、更简洁的开发框架，自带 .tool() 装饰器。
# ==============================================================================
mcp = FastMCP("my-first-mcp")

# ==============================================================================
# 2. 注册工具 (Tools)
# 使用 @mcp.tool() 装饰器。
# LLM 会读取函数下方的注释 (Docstring) 来理解工具的功能。
# ==============================================================================
@mcp.tool()
def say_hello(name: str) -> str:
    """向指定的名字打招呼，这是我们的第一个测试功能。
    
    Args:
        name: 想要打招呼的人的名字
    """
    return f"Hello {name}! 恭喜你，你的 Python FastMCP Server 已经通车啦！🚀"

# ==============================================================================
# 3. 运行服务
# FastMCP 内部自动处理了异步逻辑和 stdio 通信。
# ==============================================================================
if __name__ == "__main__":
    mcp.run()
